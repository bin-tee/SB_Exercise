// Numbers up to ten

for (x = 0; x <= 10; x += 1) {
    console.log(x);
}

for (x = 0; x <= 10; x++) {
    console.log(x);
}

let z = 0;
while (z <= 10) {
    console.log(z);
    z++;
}

let z = 0;
while (z <= 10) {
    console.log(z);
    z += 1;
}


// Even numbers to 20

for (y = 0; y <= 20; y += 2) {
    console.log(y);
}



// Odd numbers to 20

let i = 0;
while (i <= 20) {
    console.log(i);
    i += 2;
}


// Back to zero

for (u = 10; u >= 0; u--) {
    console.log(u);
}

for (u = 10; u >= 0; u -= 1) {
    console.log(u);
}